class Solution {
  public:
    vector<int> primeRange(int M, int N) {
        // code here
        vector<int> primes;
        
        for (int i = M; i <= N; i++) {
            if (i < 2) continue;
            
            bool isPrime = true;
            for (int j = 2; j * j <= i; j++) {
                if (i % j == 0) {
                    isPrime = false;
                    break;
                }
            }
            
            if (isPrime) primes.push_back(i);
        }
        
        return primes;
    }
};

